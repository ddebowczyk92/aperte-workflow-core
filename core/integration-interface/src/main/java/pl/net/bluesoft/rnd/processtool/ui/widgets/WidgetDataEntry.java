package pl.net.bluesoft.rnd.processtool.ui.widgets;

/**
 * @author: mpawlak@bluesoft.net.pl
 */
public class WidgetDataEntry
{
    private String key;
    private String value;
    private String type;
    private Boolean saveToRoot = false;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value)
    {
        this.value = value;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Boolean getSaveToRoot() {
        return saveToRoot;
    }

    public void setSaveToRoot(Boolean saveToRoot) {
        this.saveToRoot = saveToRoot;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof WidgetDataEntry)) return false;

        WidgetDataEntry that = (WidgetDataEntry) o;

        if (key != null ? !key.equals(that.key) : that.key != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        return key != null ? key.hashCode() : 0;
    }
}
